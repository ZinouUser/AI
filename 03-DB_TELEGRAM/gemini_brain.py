FALLBACK_RESPONSES = [
    "والله ما فهمت زين، عاد قولها مرة ثانية؟",
    "آسفة، ما سمعتك صح. تقدر تكرر؟",
    "عاد قولي وش قلت، أنا معك.",
]


class GeminiBrain:
    def __init__(self):
        if not GEMINI_API_KEY:
            raise EnvironmentError("GEMINI_API_KEY missing in .env")
        self._client       = genai.Client(api_key=GEMINI_API_KEY)
        self._chat         = self._new_chat()
        self._fallback_idx = 0
        self._patient_ctx  = ""
        self._protocol_ctx = ""
        self._greeted = False


    def _new_chat(self):
        return self._client.chats.create(
            model=GEMINI_MODEL,
            config=types.GenerateContentConfig(
                system_instruction=SYSTEM_PROMPT,
                temperature=0.2,
                max_output_tokens=3000
            ),
        )

    def set_context(self, patient_ctx: str = "", protocol_ctx: str = ""):
        """
        Inject patient profile + protocol context.
        Called by voice_agent_llm.py before each think() call.
        Context is prepended to the next user message.
        """
        self._patient_ctx  = patient_ctx
        self._protocol_ctx = protocol_ctx


    def prime_opening(self):
        """Mark opening greeting as already spoken — LLM will skip re-greeting."""
        self._greeted = True        

    def _call_gemini(self, text: str) -> str:
        response = self._chat.send_message(text)
        return response.text.strip()


    def think(self, user_text: str) -> str:
        
        # Greeting flag → note système en tête du message
        if self._greeted:
            greeting_note = "[تنبيه: الترحيب تم — ابدئي مباشرة بالتقييم]\n\n"
            self._greeted = False
        else:
            greeting_note = ""

        # Build message: prepend context when available
        if self._patient_ctx or self._protocol_ctx:
            prefix = ""
            if self._patient_ctx:
                prefix += f"=== ملف الطالبة ===\n{self._patient_ctx}\n\n"
            if self._protocol_ctx:
                prefix += (
                    "=== دليل التقييم (لأمل فقط — لا تذكريه للطالبة) ===\n"
                    "اسألي الطالبة الأسئلة التالية سؤالاً واحداً في كل رد:\n"
                    f"{self._protocol_ctx}\n\n"
                )
            full_text = greeting_note + prefix + f"الطالبة: {user_text}"
        else:
            full_text = greeting_note + user_text

        for attempt in range(1, GEMINI_RETRIES + 2):
            try:
                with concurrent.futures.ThreadPoolExecutor(max_workers=1) as executor:
                    future = executor.submit(self._call_gemini, full_text)
                    return future.result(timeout=GEMINI_TIMEOUT)
            except concurrent.futures.TimeoutError:
                print(f"  Gemini timeout ({GEMINI_TIMEOUT}s) — local fallback")
                return self._fallback()
            except Exception as e:
                if "429" in str(e) and attempt <= GEMINI_RETRIES:
                    print(f"  Gemini 429 — waiting {GEMINI_RETRY_DELAY}s "
                        f"(attempt {attempt}/{GEMINI_RETRIES})")
                    time.sleep(GEMINI_RETRY_DELAY)
                    continue
                print(f"  Gemini error: {e} — local fallback")
                return self._fallback()
        return self._fallback()


    def _fallback(self) -> str:
        reply = FALLBACK_RESPONSES[self._fallback_idx % len(FALLBACK_RESPONSES)]
        self._fallback_idx += 1
        return reply
    

    def reset(self):
        """Clear conversation history and context for a new student session."""
        self._chat         = self._new_chat()
        self._fallback_idx = 0
        self._patient_ctx  = ""
        self._protocol_ctx = ""
        self._greeted      = False
