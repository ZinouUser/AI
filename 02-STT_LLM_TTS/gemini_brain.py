FALLBACK_RESPONSES = [
    "والله ما فهمت زين، عاد قولها مرة ثانية؟",
    "آسفة، ما سمعتك صح. تقدر تكرر؟",
    "عاد قولي وش قلت، أنا معك.",
]


class GeminiBrain:
    def __init__(self):
        if not GEMINI_API_KEY:
            raise EnvironmentError("GEMINI_API_KEY missing in .env")
        self._client = genai.Client(api_key=GEMINI_API_KEY)
        self._chat = self._new_chat()
        self._fallback_idx = 0

    def _new_chat(self):
        return self._client.chats.create(
            model=GEMINI_MODEL,
            config=types.GenerateContentConfig(
                system_instruction=SYSTEM_PROMPT,
                temperature=0.2,
                max_output_tokens=2000,
            ),
        )

    def _call_gemini(self, text: str) -> str:
        response = self._chat.send_message(text)
        return response.text.strip()

    def think(self, user_text: str) -> str:
        for attempt in range(1, GEMINI_RETRIES + 2):
            try:
                with concurrent.futures.ThreadPoolExecutor(max_workers=1) as executor:
                    future = executor.submit(self._call_gemini, user_text)
                    return future.result(timeout=GEMINI_TIMEOUT)
            except concurrent.futures.TimeoutError:
                print(f"  Gemini timeout ({GEMINI_TIMEOUT}s) — local fallback")
                return self._fallback()
            except Exception as e:
                if "429" in str(e) and attempt <= GEMINI_RETRIES:
                    print(f"  Gemini 429 — waiting {GEMINI_RETRY_DELAY}s "
                          f"(attempt {attempt}/{GEMINI_RETRIES})")
                    time.sleep(GEMINI_RETRY_DELAY)
                    continue
                print(f"  Gemini error: {e} — local fallback")
                return self._fallback()
        return self._fallback()

    def _fallback(self) -> str:
        reply = FALLBACK_RESPONSES[self._fallback_idx % len(FALLBACK_RESPONSES)]
        self._fallback_idx += 1
        return reply

    def reset(self):
        self._chat = self._new_chat()
        self._fallback_idx = 0
